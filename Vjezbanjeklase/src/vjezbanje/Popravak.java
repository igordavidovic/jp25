package vjezbanje;

public class Popravak {
	private Urar urar;
	private Sat sat;
	private int trajanje;
	private double cijena;
	
	public Urar getUrar() {
		return urar;
	}
	public void setUrar(Urar urar) {
		this.urar = urar;
	}
	public Sat getSat() {
		return sat;
	}
	public void setSat(Sat sat) {
		this.sat = sat;
	}
	public int getTrajanje() {
		return trajanje;
	}
	public void setTrajanje(int trajanje) {
		this.trajanje = trajanje;
	}
	public double getCijena() {
		return cijena;
	}
	public void setCijena(double cijena) {
		this.cijena = cijena;
	}
	
	
}
